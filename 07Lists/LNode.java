public class LNode{
    private int value;
    private LNode next = null;

    public LNode(int value){
	setValue(value);
    }

    public int getValue(){
	return value;
    }

    public void setValue(int x){
	value = x;
    }

    public LNode getNext(){
	return next;
    }

    public void setNext(LNode a){
	next = a;
    }

    public String toString(){
	return "" + this.getValue();
    }
}
